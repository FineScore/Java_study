
public class VarEx1 {

	public static void main(String[] args) {
		int year = 0; // 정수형 변수 year에 0을 초기화 
		int age = 14; // 정수형 변수 age에 14를 초기화
		
		System.out.println(year);
		System.out.println(age);
		
		year = age + 2000; // age에 2000을 더해서 year에 저장
		age = age + 1; // age에 저장한 값 1 증가
		
		System.out.println(year);
		System.out.println(age);

	}

}
